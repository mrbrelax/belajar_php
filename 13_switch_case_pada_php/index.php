Penggunaan Switch Case PHP :
 -->
<?php
$name = "Mr.B";
switch($name){
    case 'Andi':
        echo 'namanya andi';
        break;
    case 'Tika':
        echo 'namanya tika';
        break;
    case 'Mr.B':
        echo 'namanya Mr.B';
        break;
    default:
        echo 'Tidak ada yang cocok';
}
?>
