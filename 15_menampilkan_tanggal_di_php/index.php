<?php
// Menampilkan tanggal di PHP :
echo "Menampilkan tanggal:";
echo "<br/>";
echo date('l, d-m-Y');
echo "<br/>";
echo date('d / M / y');
echo "<br/>";
echo date('D - M / Y');

// Menampilkan waktu di PHP :
echo "<br/> <br/><br/>";
echo "Menampilkan waktu:";
echo "<br/>";
echo date('H:i:s a');
echo "<br/>";
// kombinasi format tanggal dan jam :
echo date('l, d-m-Y h:i:s a');
?>

